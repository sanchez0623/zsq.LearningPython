# 进制

# 二进制
# 使用0b开头 b表示binary
print(0b10);

#八进制
# 使用0o开头 o表示octonary
print(0o10);

#十六进制
# 使用0x开头 x表示hex
print(0x10);

#进制转换
#用py内置函数bin,int,hex

print(bin(10));
print(bin(0b10));
print(bin(0x10));

print(int(0b10));
print(int(0o10));
print(int(0x10));

print(hex(0b10));
print(hex(0o10));
print(hex(10));

print(oct(0b10));
print(oct(10));
print(oct(0x10));